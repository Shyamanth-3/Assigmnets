//Increment and Decrement Operators
let a = 5;
console.log("Initial value of a: ",a);
console.log("Pre-increment (++a):", ++a); // Increment first, then use
console.log("Post-increment (a++):", a++); // Use first, then increment
console.log("Pre-decrement (--a):", --a); // Decrement first, then use
console.log("Post-decrement (a--):", a--); // Use first, then decrement
console.log("Final value of a:", a);
